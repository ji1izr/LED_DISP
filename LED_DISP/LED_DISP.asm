#include <m88Pdef.inc>
;clock 1MHz

;<define resistors>
.def	Work_Main	=	R16
.def	Work_Led	=	R17
.def	Work_Sel	=	R18
.def	Work_Wait	=	R19
.def	Wait_Ms		=	R20
.def	Chr_Out		=	R21
.def	Count		=	R22
.def	Month		=	R0
.def	Day10		=	R1
.def	Day01		=	R2
.def	Hour10		=	R3
.def	Hour01		=	R4
.def	Minute10	=	R5
.def	Minute01	=	R6
.def	Second10	=	R7
.def	Second01	=	R8
.def	GyroX		=	R9
.def	GyroY		=	R10
.def	GyroZ		=	R11

.def	Sreg_Bkup	=	R15

.equ	LED_row	=	PORTB
.equ	LED_row_0	=	PORTB0
.equ	LED_row_1	=	PORTB1
.equ	LED_row_2	=	PORTB2

.equ	LED_select	=	PORTD

.equ	LCD			=	PORTC
.equ	LCD_rs		=	PORTC5
.equ	LCD_e		=	PORTC4
.equ	LCD_db7		=	PORTC3
.equ	LCD_db6		=	PORTC2
.equ	LCD_db5		=	PORTC1
.equ	LCD_db4		=	PORTC0

.equ	Interval	=	0xFC	;0xFC is about 1sec 0xFF is 0.25sec

;<program area>
.cseg

;<inturrupt table>
.org 0
		rjmp	RESET	;Reset
		rjmp	ERR		;IREQ0
		rjmp	ERR		;IREQ1
		rjmp	ERR		;PCINT0
		rjmp	ERR		;PCINT1
		rjmp	ERR		;PCINT2
		rjmp	ERR		;Watchdog Timer
		rjmp	ERR		;Timer2 Compare A
		rjmp	ERR		;Timer2 Compare B
		rjmp	ERR		;Timer2 Overflow
		rjmp	ERR		;TImer1 Capture
		rjmp	ERR		;Timer1 Compare A
		rjmp	ERR		;Timer1 Compare B
		rjmp	LED		;Timer1 Overflow
		rjmp	ERR		;Timer0 Compare A
		rjmp	ERR		;Timer0 Compare B
		rjmp	SEL		;Timer0 Overflow
		rjmp	ERR		;SPI Transfer Complete
		rjmp	ERR		;USART RX Complete
		rjmp	ERR		;USART UDR Empty
		rjmp	ERR		;USART TX Complete
		rjmp	ERR		;ADC Conversion Complete
		rjmp	ERR		;EEPROM Ready
		rjmp	ERR		;Analog Comparator
		rjmp	ERR		;2-wire Serial Interface
		rjmp	ERR		;Store Program Memory Ready
	;
	RESET:
		;<inhibit inturuppt>
		cli
		;
		;<initialize stuck pointer>
		ldi	Work_Main,high(RAMEND)
		out	SPH,Work_Main
		ldi	Work_Main,low(RAMEND)
		out	SPL,Work_Main
		;
		;<PORTB initialize>
		;all bits are output
		;initial value is '0'
		ldi	Work_Main,0b11111111
		out	DDRB,Work_Main
		ldi	Work_Main,0b00000000
		out	PORTB,Work_Main	;PORTB=LED_row
		;
		;<PORTC initialize>
		;all bits are output
		;initial value is '0'
		ldi	Work_Main,0b11111111
		out	DDRC,Work_Main
		ldi	Work_Main,0b00000000
		out	PORTC,Work_Main	;PORTC=LCD
		;
		;<PORTD initialize>
		;all bits are output
		;initial value is '1'
		ldi	Work_Main,0b11111111
		out	DDRD,Work_Main
		ldi	Work_Main,0b11111111
		out	PORTD,Work_Main	;PORTD=LED_select
		;
		;<Timer0 initialize>
		;stop timer0
		ldi	Work_Main,0b00000000
		out	TCCR0B,Work_Main
		;set Timer0 as 0
		ldi	Work_Main,0
		out	TCNT0,Work_Main
		;Timer0 overflow interrupt enable
		ldi	Work_Main,0b00000001
		sts	TIMSK0,Work_Main
		;set prescaler as 1/8 and start Timer0
		ldi Work_Main,0b00000010
		out	TCCR0B,Work_Main
		;
		;<Timer1 initialize>
		;stop	Timer1
		ldi	Work_Main,0b00000000
		sts	TCCR1B,Work_Main
		;set Timer1 as 0xFC00 for about 1 sec
		ldi	Work_Main,Interval
		sts	TCNT1H,Work_Main
		ldi	Work_Main,0x00
		sts	TCNT1L,Work_Main
		;Timer1 overflow interrupt eneble
		ldi	Work_Main,0b00000001
		sts	TIMSK1,Work_Main
		;set prescaler as 1/1024 and star Timer1
		ldi	Work_Main,0b00000101
		sts	TCCR1B,Work_Main
		;
		;<LCD initialize>
		cbi	LCD,LCD_rs	;instruction
		ldi	Chr_Out,0b00000011
		rcall	LCD_OUT
		ldi	Wait_Ms,40	;wait about 1ms
		rcall	WAIT
		ldi	Chr_Out,0b00000011
		rcall	LCD_OUT
		ldi	Wait_Ms,40	;wait about 1ms
		rcall	WAIT
		ldi	Chr_Out,0b00000011
		rcall	LCD_OUT
		ldi	Wait_Ms,40	;wait about 1ms
		rcall	WAIT
		ldi	Chr_Out,0b00000010	;set 4 bits mode
		rcall	LCD_OUT
		ldi	Chr_Out,0b00000010	;set 2 lines and 5*8 dot font set
		rcall	LCD_OUT
		ldi	Chr_Out,0b00001000
		rcall	LCD_OUT
		ldi	Chr_Out,0b00000000	;display off
		rcall	LCD_OUT
		ldi	Chr_Out,0b00001000
		rcall	LCD_OUT
		ldi	Chr_Out,0b00000000	;display clear
		rcall	LCD_OUT
		ldi	Chr_Out,0b00000001
		rcall	LCD_OUT
		ldi	Chr_Out,0b00000000	;entry mode as increment
		rcall	LCD_OUT
		ldi	Chr_Out,0b00000110
		rcall	LCD_OUT
		ldi	Chr_Out,0b00000000	;display on
		rcall	LCD_OUT
		ldi	Chr_Out,0b00001100
		rcall	LCD_OUT
		ldi	Chr_Out,0b00000000	;cursor at home
		rcall	LCD_OUT
		ldi	Chr_Out,0b00000010
		rcall	LCD_OUT
		;
		;<set the position of the message table to Z>
		ldi	ZH,HIGH(TITLE)
		ldi	ZL,LOW(TITLE)
		;shift left because the ROM address is twice
		clc
		rol	ZL
		rol	ZH
		;
		ldi	Count,16
		sbi	LCD,LCD_rs	;select data
		;
	MSG_OUT:
		lpm	Chr_Out,Z+
		swap	Chr_Out
		rcall	LCD_OUT
		swap	Chr_Out
		rcall	LCD_OUT
		dec	Count
		brne	MSG_OUT
		cbi	LCD,LCD_rs	;select control
		;
		;<set the position of the data table to Z>
		ldi	ZH,HIGH(GYRO)
		ldi	ZL,LOW(GYRO)
		;shift left because the ROM address is twice
		clc
		rol	ZL
		rol	ZH
