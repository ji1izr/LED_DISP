		cbi	LCD,LCD_rs	;select control
		;
		;
		out	SREG,Sreg_Bkup
;		sei
		reti
		;

	SEL:
		;<select LED array>
;		cli
		in	Sreg_Bkup,SREG
		;
		;<clear Timer0>
		;stop timer0
		ldi	Work_Led,0b00000000
		out	TCCR0B,Work_Led
		;set Timer0 as 0
		ldi	Work_Led,0
		out	TCNT0,Work_Led
		;set prescaler as 1/8 and start Timer0
		ldi	Work_Led,0b00000010
		out	TCCR0B,Work_Led
		;
		;<select next LED array>
		;check formar selected LED
		in	Work_Sel,LED_row
		;shift next LED array
		lsl	Work_Sel
		;if LED array 2 was selected
		sbrc	Work_Sel,LED_row_2+1
		;select LED array 0
		ldi	Work_Sel,0b00000001
		;mask 0:2 bits
		andi	Work_Sel,0b00000111
		;clear LED
		cbi	LED_row,LED_row_0
		cbi	LED_row,LED_row_1
		cbi	LED_row,LED_row_2
		;select data
		sbrc	Work_Sel,LED_row_0
		out	LED_select,GyroX
		sbrc	Work_Sel,LED_row_1
		out	LED_select,GyroY
		sbrc	Work_Sel,LED_row_2
		out	LED_select,GyroZ
		;
		out	LED_row,Work_Sel
		;
		out	SREG,Sreg_Bkup
;		sei
		reti
		;

	ERR:
		;<errors>
		reti
		;

