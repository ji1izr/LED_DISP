		mov	Day10,Day01
		lsr	Day10
		lsr	Day10
		lsr	Day10
		lsr	Day10
		ldi	Work_Main,0x0F	;change value into ASCII
		and	Day10,Work_Main
		and	Day01,Work_Main
		ldi	Work_Main,0x30
		or	Day10,Work_Main
		or	Day01,Work_Main
		;
		lpm	Hour01,Z+
		mov	Hour10,Hour01
		lsr	Hour10
		lsr	Hour10
		lsr	Hour10
		lsr	Hour10
		ldi	Work_Main,0x0F	;change value into ASCII
		and	Hour10,Work_Main
		and	Hour01,Work_Main
		ldi	Work_Main,0x30
		or	Hour10,Work_Main
		or	Hour01,Work_Main
		;
		lpm	Minute01,Z+
		mov	Minute10,Minute01
		lsr	Minute10
		lsr	Minute10
		lsr	Minute10
		lsr	Minute10
		ldi	Work_Main,0x0F	;change value into ASCII
		and	Minute10,Work_Main
		and	Minute01,Work_Main
		ldi	Work_Main,0x30
		or	Minute10,Work_Main
		or	Minute01,Work_Main
		;
		lpm	Second01,Z+
		mov	Second10,Second01
		lsr	Second10
		lsr	Second10
		lsr	Second10
		lsr	Second10
		ldi	Work_Main,0x0F	;change value into ASCII
		and	Second10,Work_Main
		and	Second01,Work_Main
		ldi	Work_Main,0x30
		or	Second10,Work_Main
		or	Second01,Work_Main
		;
		lpm	GyroX,Z+
		com	GyroX
		lpm	GyroY,Z+
		com	GyroY
		lpm	GyroZ,Z+
		com	GyroZ
		;
		;<set initial data on LED>
		out	LED_select,GyroX
		sbi	LED_row,LED_row_0
		;
		;
		;<set initial data on the 2nd line of LCD>
		cbi	LCD,LCD_rs	;select control
		ldi	Chr_Out,0xC0
		swap	Chr_Out
		rcall	LCD_OUT
		swap	Chr_Out
		rcall	LCD_OUT
		;
		sbi	LCD,LCD_rs	;select data
		;
		ldi	Chr_Out,' '
		swap	Chr_Out
		rcall	LCD_OUT
		swap	Chr_Out
		rcall	LCD_OUT
		;
		mov	Chr_Out,Month
		swap	Chr_Out
		rcall	LCD_OUT
		swap	Chr_Out
		rcall	LCD_OUT
		;
		ldi	Chr_Out,'/'
		swap	Chr_Out
		rcall	LCD_OUT
		swap	Chr_Out
		rcall	LCD_OUT
		;
		mov	Chr_Out,Day10
		swap	Chr_Out
		rcall	LCD_OUT
		swap	Chr_Out
		rcall	LCD_OUT
		;
		mov	Chr_Out,Day01
		swap	Chr_Out
		rcall	LCD_OUT
		swap	Chr_Out
		rcall	LCD_OUT
		;
		ldi	Chr_Out,' '
		swap	Chr_Out
		rcall	LCD_OUT
		swap	Chr_Out
		rcall	LCD_OUT
		;
		mov	Chr_Out,Hour10
		swap	Chr_Out
		rcall	LCD_OUT
		swap	Chr_Out
		rcall	LCD_OUT
		;
		mov	Chr_Out,Hour01
		swap	Chr_Out
		rcall	LCD_OUT
		swap	Chr_Out
		rcall	LCD_OUT
		;
		ldi	Chr_Out,':'
		swap	Chr_Out
		rcall	LCD_OUT
		swap	Chr_Out
		rcall	LCD_OUT
		;
		mov	Chr_Out,Minute10
		swap	Chr_Out
		rcall	LCD_OUT
		swap	Chr_Out
		rcall	LCD_OUT
		;
		mov	Chr_Out,Minute01
		swap	Chr_Out
		rcall	LCD_OUT
		swap	Chr_Out
		rcall	LCD_OUT
		;
		ldi	Chr_Out,':'
		swap	Chr_Out
		rcall	LCD_OUT
		swap	Chr_Out
		rcall	LCD_OUT
		;
		mov	Chr_Out,Second10
		swap	Chr_Out
		rcall	LCD_OUT
		swap	Chr_Out
		rcall	LCD_OUT
		;
		mov	Chr_Out,Second01
		swap	Chr_Out
		rcall	LCD_OUT
		swap	Chr_Out
		rcall	LCD_OUT
		;
		cbi	LCD,LCD_rs	;select control
		;
		;<enable interrupt>
		sei
		;
	LOOP:
		;
		;<sleep>
		sleep
		;
		;
		rjmp	LOOP

	WAIT:;<wait Wait_Ms/7.812 ms>
		;stop Timer2
		ldi	Work_Wait,0b00000000
		sts	TCCR2B,Work_Main
		;set Timer2 as 0
		ldi	Work_Wait,0
		sts	TCNT2,Work_Wait
		;set prescaler as 1/128 and start Timer2
		ldi Work_Main,0b00000101
		sts	TCCR2B,Work_Main
	WAIT_LOOP:
		;wait while TCNT2 < Wait_Ms
		lds	Work_Wait,TCNT2
		cp	Work_Wait,Wait_Ms
		brlo	WAIT_LOOP
		;stop Timer2
		ldi	Work_Wait,0b00000000
		sts	TCCR2B,Work_Main
		ret

	LCD_OUT:
		;<send lower 4 bits of Chr_Out to LCD>
		cbi	LCD,LCD_e
		cbi	LCD,LCD_db7	;0
		cbi	LCD,LCD_db6	;0
		cbi	LCD,LCD_db5	;0
		cbi	LCD,LCD_db4	;0
		sbrc	Chr_Out,3
		sbi	LCD,LCD_db7
		sbrc	Chr_Out,2
		sbi	LCD,LCD_db6
		sbrc	Chr_Out,1
		sbi	LCD,LCD_db5
		sbrc	Chr_Out,0
		sbi	LCD,LCD_db4
		sbi	LCD,LCD_e
		ldi	Wait_Ms,8	;128*8=1.024ms
		rcall	WAIT
		cbi	LCD,LCD_e
		ldi	Wait_Ms,8	;128*8=1.024ms
		rcall	WAIT
		ret


	LED:
		;<out LED selected>
		;
;		cli
		in	Sreg_Bkup,SREG
		;
		;<set Timer1>
		;stop Timer1
		ldi	Work_Sel,0b00000000
		sts	TCCR1B,Work_Sel
		;set Timer1 as 0xFC00 for about 1 sec
		ldi	Work_Sel,Interval
		sts	TCNT1H,Work_Sel
		ldi	Work_Sel,0x00
		sts	TCNT1L,Work_Sel
		;set prescaler as 1/1024 and star Timer1
		ldi	Work_Sel,0b00000101
		sts	TCCR1B,Work_Sel
		;
		;<load and out data>
		lpm	Month,Z+
		ldi	Work_Main,0x0F	;change value into ASCII
		and	Month,Work_Main
		ldi	Work_Main,0x30
		or	Month,Work_Main
		;
		lpm	Day01,Z+
		mov	Day10,Day01
		lsr	Day10
		lsr	Day10
		lsr	Day10
		lsr	Day10
		ldi	Work_Main,0x0F	;change value into ASCII
		and	Day10,Work_Main
		and	Day01,Work_Main
		ldi	Work_Main,0x30
		or	Day10,Work_Main
		or	Day01,Work_Main
		;
		lpm	Hour01,Z+
		mov	Hour10,Hour01
		lsr	Hour10
		lsr	Hour10
		lsr	Hour10
		lsr	Hour10
		ldi	Work_Main,0x0F	;change value into ASCII
		and	Hour10,Work_Main
		and	Hour01,Work_Main
		ldi	Work_Main,0x30
		or	Hour10,Work_Main
		or	Hour01,Work_Main
		;
		lpm	Minute01,Z+
		mov	Minute10,Minute01
		lsr	Minute10
		lsr	Minute10
		lsr	Minute10
		lsr	Minute10
		ldi	Work_Main,0x0F	;change value into ASCII
		and	Minute10,Work_Main
		and	Minute01,Work_Main
		ldi	Work_Main,0x30
		or	Minute10,Work_Main
		or	Minute01,Work_Main
		;
		lpm	Second01,Z+
		mov	Second10,Second01
		lsr	Second10
		lsr	Second10
		lsr	Second10
		lsr	Second10
		ldi	Work_Main,0x0F	;change value into ASCII
		and	Second10,Work_Main
		and	Second01,Work_Main
		ldi	Work_Main,0x30
		or	Second10,Work_Main
		or	Second01,Work_Main
		;
		lpm	GyroX,Z+
		com	GyroX
		lpm	GyroY,Z+
		com	GyroY
		lpm	GyroZ,Z+
		com	GyroZ
		;
		;check data end
		mov	Work_Led,Month
;		cpi	Work_Led,255
		cpi	Work_Led,0x3F	;0xFF changed to x3F
		brne	LED_NEXT
		;
		;if data end set initial parameters
		ldi	ZH,HIGH(GYRO)
		ldi	ZL,LOW(GYRO)
		;shift left because the ROM address is twice
		clc
		rol	ZL
		rol	ZH
		;
	LED_NEXT:
		;
		;<set data on the 2nd line of LCD>
		cbi	LCD,LCD_rs	;select control
		ldi	Chr_Out,0xC0
		swap	Chr_Out
		rcall	LCD_OUT
		swap	Chr_Out
		rcall	LCD_OUT
		;
		sbi	LCD,LCD_rs	;select data
		;
		ldi	Chr_Out,' '
		swap	Chr_Out
		rcall	LCD_OUT
		swap	Chr_Out
		rcall	LCD_OUT
		;
		mov	Chr_Out,Month
		swap	Chr_Out
		rcall	LCD_OUT
		swap	Chr_Out
		rcall	LCD_OUT
		;
		ldi	Chr_Out,'/'
		swap	Chr_Out
		rcall	LCD_OUT
		swap	Chr_Out
		rcall	LCD_OUT
		;
		mov	Chr_Out,Day10
		swap	Chr_Out
		rcall	LCD_OUT
		swap	Chr_Out
		rcall	LCD_OUT
		;
		mov	Chr_Out,Day01
		swap	Chr_Out
		rcall	LCD_OUT
		swap	Chr_Out
		rcall	LCD_OUT
		;
		ldi	Chr_Out,' '
		swap	Chr_Out
		rcall	LCD_OUT
		swap	Chr_Out
		rcall	LCD_OUT
		;
		mov	Chr_Out,Hour10
		swap	Chr_Out
		rcall	LCD_OUT
		swap	Chr_Out
		rcall	LCD_OUT
		;
		mov	Chr_Out,Hour01
		swap	Chr_Out
		rcall	LCD_OUT
		swap	Chr_Out
		rcall	LCD_OUT
		;
		ldi	Chr_Out,':'
		swap	Chr_Out
		rcall	LCD_OUT
		swap	Chr_Out
